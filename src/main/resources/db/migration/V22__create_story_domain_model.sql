    CONSTRAINT ck_story_pair_decisions_similarity
        CHECK (cosine_similarity >= -1 AND cosine_similarity <= 1),
    CONSTRAINT ck_story_pair_decisions_time CHECK (time_distance_seconds >= 0),
    CONSTRAINT ck_story_pair_decisions_rank CHECK (candidate_rank IS NULL OR candidate_rank > 0),
    CONSTRAINT ck_story_pair_decisions_window CHECK (candidate_window_hours > 0),
    CONSTRAINT ck_story_pair_decisions_threshold
        CHECK (similarity_threshold >= -1 AND similarity_threshold <= 1),
    CONSTRAINT ck_story_pair_decisions_result CHECK (result IN ('SAME_STORY', 'UNCERTAIN')),
    CONSTRAINT ck_story_pair_decisions_sources CHECK (
        left_effective_at_source IN ('PUBLISHED_AT', 'FIRST_SEEN_AT')
        AND right_effective_at_source IN ('PUBLISHED_AT', 'FIRST_SEEN_AT')
    ),
    CONSTRAINT ck_story_pair_decisions_top_one CHECK (
        NOT top_one_below_threshold OR result = 'UNCERTAIN'
    )
);

