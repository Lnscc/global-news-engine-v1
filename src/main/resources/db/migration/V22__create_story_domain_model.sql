    CONSTRAINT uq_story_pair_decisions_hash UNIQUE (decision_hash),
    CONSTRAINT uq_story_pair_decisions_pair
        UNIQUE (snapshot_id, left_article_ref, right_article_ref, pair_rule_version),
    CONSTRAINT fk_story_pair_decisions_run
        FOREIGN KEY (run_id, clustering_version_id)
        REFERENCES story_processing_runs(id, clustering_version_id),
    CONSTRAINT fk_story_pair_decisions_snapshot
        FOREIGN KEY (snapshot_id, clustering_version_id)
        REFERENCES story_snapshots(id, clustering_version_id),
    CONSTRAINT fk_story_pair_decisions_left_input
        FOREIGN KEY (left_article_input_id, clustering_version_id)
        REFERENCES story_article_inputs(id, clustering_version_id),
    CONSTRAINT fk_story_pair_decisions_right_input
        FOREIGN KEY (right_article_input_id, clustering_version_id)
        REFERENCES story_article_inputs(id, clustering_version_id),
    CONSTRAINT fk_story_pair_decisions_left_snapshot_member
        FOREIGN KEY (snapshot_id, left_article_input_id)
        REFERENCES story_snapshot_members(snapshot_id, article_input_id),
    CONSTRAINT fk_story_pair_decisions_right_snapshot_member
        FOREIGN KEY (snapshot_id, right_article_input_id)
        REFERENCES story_snapshot_members(snapshot_id, article_input_id),
    CONSTRAINT fk_story_pair_decisions_left_embedding
        FOREIGN KEY (left_embedding_artifact_id, left_vector_hash)
        REFERENCES story_embedding_artifacts(id, vector_hash),
    CONSTRAINT fk_story_pair_decisions_right_embedding
        FOREIGN KEY (right_embedding_artifact_id, right_vector_hash)
        REFERENCES story_embedding_artifacts(id, vector_hash),
    CONSTRAINT ck_story_pair_decisions_hash CHECK (CHAR_LENGTH(decision_hash) = 64),
    CONSTRAINT ck_story_pair_decisions_order CHECK (left_article_ref < right_article_ref),
    CONSTRAINT ck_story_pair_decisions_fingerprints CHECK (
        CHAR_LENGTH(left_article_input_fingerprint) = 64
        AND CHAR_LENGTH(right_article_input_fingerprint) = 64
