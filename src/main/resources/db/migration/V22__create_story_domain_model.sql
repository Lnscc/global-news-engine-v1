    run_id BIGINT NOT NULL,
    snapshot_id BIGINT NOT NULL,
    left_article_input_id BIGINT NOT NULL,
    right_article_input_id BIGINT NOT NULL,
    left_article_ref VARCHAR(64) NOT NULL,
