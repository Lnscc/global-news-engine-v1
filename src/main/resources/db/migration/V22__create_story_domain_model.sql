CREATE TABLE story_clustering_version_status_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clustering_version_id BIGINT NOT NULL REFERENCES story_clustering_versions(id),
    previous_status VARCHAR(16),
    new_status VARCHAR(16) NOT NULL,
    reason TEXT NOT NULL,
    changed_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT ck_story_clustering_status_history_previous
        CHECK (previous_status IS NULL OR previous_status IN ('SHADOW', 'ACTIVE', 'RETIRED')),
    CONSTRAINT ck_story_clustering_status_history_new
        CHECK (new_status IN ('SHADOW', 'ACTIVE', 'RETIRED')),
    CONSTRAINT ck_story_clustering_status_history_transition CHECK (
        (previous_status IS NULL AND new_status = 'SHADOW')
        OR (previous_status = 'SHADOW' AND new_status = 'ACTIVE')
        OR (previous_status = 'ACTIVE' AND new_status = 'RETIRED')
        OR (previous_status = 'RETIRED' AND new_status = 'ACTIVE')
    )
);

CREATE TABLE story_embedding_artifacts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    embedding_model_id VARCHAR(128) NOT NULL,
    embedding_model_version VARCHAR(255) NOT NULL,
    embedding_dimension INTEGER NOT NULL,
    title_normalization_version VARCHAR(128) NOT NULL,
    title_input_hash VARCHAR(64) NOT NULL,
    status VARCHAR(32) NOT NULL,
    vector_bytes BYTEA,
    vector_hash VARCHAR(64),
    vector_norm NUMERIC(20, 10),
    provider_request_id VARCHAR(255),
    attempt_count INTEGER NOT NULL DEFAULT 0,
    next_retry_at TIMESTAMP WITH TIME ZONE,
    ready_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT uq_story_embedding_artifacts_key UNIQUE (
        embedding_model_id,
        embedding_model_version,
        embedding_dimension,
        title_normalization_version,
        title_input_hash
    ),
    CONSTRAINT uq_story_embedding_artifacts_id_hash UNIQUE (id, vector_hash),
    CONSTRAINT ck_story_embedding_artifacts_dimension CHECK (embedding_dimension > 0),
    CONSTRAINT ck_story_embedding_artifacts_title_hash CHECK (CHAR_LENGTH(title_input_hash) = 64),
    CONSTRAINT ck_story_embedding_artifacts_status
        CHECK (status IN ('PENDING', 'READY', 'RETRYABLE_FAILURE', 'TERMINAL_FAILURE')),
    CONSTRAINT ck_story_embedding_artifacts_attempt_count CHECK (attempt_count >= 0),
    CONSTRAINT ck_story_embedding_artifacts_payload CHECK (
        (status = 'READY'
            AND vector_bytes IS NOT NULL
            AND OCTET_LENGTH(vector_bytes) = embedding_dimension * 4
            AND vector_hash IS NOT NULL
            AND CHAR_LENGTH(vector_hash) = 64
            AND vector_norm > 0
            AND ready_at IS NOT NULL
            AND next_retry_at IS NULL)
        OR (status <> 'READY'
            AND vector_bytes IS NULL
            AND vector_hash IS NULL
            AND vector_norm IS NULL
            AND ready_at IS NULL)
    ),
    CONSTRAINT ck_story_embedding_artifacts_retry CHECK (
        status = 'RETRYABLE_FAILURE' OR next_retry_at IS NULL
    )
);

