    candidate_rank INTEGER,
    candidate_window_hours INTEGER NOT NULL,
    similarity_threshold NUMERIC(7, 6) NOT NULL,
    pair_rule_version VARCHAR(128) NOT NULL,
    result VARCHAR(32) NOT NULL,
