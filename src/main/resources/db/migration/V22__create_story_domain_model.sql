    CONSTRAINT ck_story_pair_decisions_title_hashes CHECK (
        CHAR_LENGTH(left_title_input_hash) = 64
        AND CHAR_LENGTH(right_title_input_hash) = 64
    ),
    CONSTRAINT ck_story_pair_decisions_vector_hashes CHECK (
        CHAR_LENGTH(left_vector_hash) = 64
        AND CHAR_LENGTH(right_vector_hash) = 64
