    CONSTRAINT uq_story_memberships_interval
        UNIQUE (clustering_version_id, article_ref, valid_from_run_id),
    CONSTRAINT ck_story_memberships_fingerprint
        CHECK (CHAR_LENGTH(article_input_fingerprint) = 64),
    CONSTRAINT ck_story_memberships_current CHECK (
        (current_marker = 1 AND valid_to_run_id IS NULL AND ended_at IS NULL)
        OR (current_marker IS NULL AND valid_to_run_id IS NOT NULL AND ended_at IS NOT NULL)
    ),
    CONSTRAINT ck_story_memberships_interval CHECK (
        valid_to_run_id IS NULL OR valid_to_run_id <> valid_from_run_id
    )
);

CREATE TABLE story_lineage (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clustering_version_id BIGINT NOT NULL,
    predecessor_story_id UUID NOT NULL,
    successor_story_id UUID NOT NULL,
    run_id BIGINT NOT NULL,
    reason VARCHAR(32) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_story_lineage_predecessor
        FOREIGN KEY (predecessor_story_id, clustering_version_id)
        REFERENCES stories(id, clustering_version_id),
    CONSTRAINT fk_story_lineage_successor
        FOREIGN KEY (successor_story_id, clustering_version_id)
        REFERENCES stories(id, clustering_version_id),
    CONSTRAINT fk_story_lineage_run
        FOREIGN KEY (run_id, clustering_version_id)
        REFERENCES story_processing_runs(id, clustering_version_id),
    CONSTRAINT uq_story_lineage_edge
        UNIQUE (clustering_version_id, predecessor_story_id, successor_story_id, run_id),
    CONSTRAINT ck_story_lineage_distinct CHECK (predecessor_story_id <> successor_story_id),
    CONSTRAINT ck_story_lineage_reason CHECK (reason IN ('MERGE', 'SPLIT', 'REPROCESSING'))
);

CREATE TABLE story_state_changes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clustering_version_id BIGINT NOT NULL,
    story_id UUID NOT NULL,
    run_id BIGINT NOT NULL,
    previous_state VARCHAR(16),
    new_state VARCHAR(16) NOT NULL,
    reason VARCHAR(128) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_story_state_changes_story
        FOREIGN KEY (story_id, clustering_version_id) REFERENCES stories(id, clustering_version_id),
    CONSTRAINT fk_story_state_changes_run
        FOREIGN KEY (run_id, clustering_version_id)
        REFERENCES story_processing_runs(id, clustering_version_id),
    CONSTRAINT uq_story_state_changes_run UNIQUE (story_id, run_id, new_state),
    CONSTRAINT ck_story_state_changes_previous
        CHECK (previous_state IS NULL OR previous_state IN ('ACTIVE', 'CLOSED', 'SUPERSEDED')),
    CONSTRAINT ck_story_state_changes_new
        CHECK (new_state IN ('ACTIVE', 'CLOSED', 'SUPERSEDED')),
    CONSTRAINT ck_story_state_changes_transition CHECK (
        (previous_state IS NULL AND new_state = 'ACTIVE')
        OR (previous_state = 'ACTIVE' AND new_state IN ('CLOSED', 'SUPERSEDED'))
        OR (previous_state = 'CLOSED' AND new_state IN ('ACTIVE', 'SUPERSEDED'))
    )
);

